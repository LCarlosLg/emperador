public class Lista {
    
    /*
    *Separa una cadena de texto apartir de la expresion o signo coma.
    *@param valor cadena de texto que sera separada a partir de el signo coma.
    *@return array de string en donde cada posicion representa una division de cadena de texto.
    */
    
    
    public String[] ordenar (String alumnos){
        String[] elementos = alumnos.split(",");
        
        
        return elementos;
        
        
    }
   
    
}
